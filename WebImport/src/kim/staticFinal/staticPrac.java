package kim.staticFinal;

public class staticPrac {
	public static void main(String[] args) {
		/*
		  public 어느곳이든 접근 가능한 메소드 
		  Java 가 최초 호출해야 하는 메소드가 main()메소드
		  
		  static 
		  main은 클래스 메소드로 MyClass가 인스턴스로 만들어지지 전이라도 얼마든지 클래스 이름으로 접근해서 사용할수 있다는 말임
		  staticPrac.main() 이라는 명령으로 호출이 가능
		 
		 String[] args java 에 결과를 돌려주지는 않아서 void 이지만 실행할떄 Java에 인자 값을 전달해서 main()함수를 호출할수는 있다
		 그래서 args 라는 변수는 문자열로 구성된 배열인 변수이고, main()메소드를 호출할수 있음
		 
		 args 라는 변수는 문자열로 구성된 배열인 변수이고 여기에 값을 넣어서 main() 메서드를 호출하는것임
		 우리는 이제껏 main() 함수에 아무값도 전달하지 않고 호출함
		 그래서 실질적으로는 public static void main()이 되어야 하는데 Java에서는 인자값을 넘겨주든 안넘겨주든 
		 public static void main(String[] args) 라고 무조건 문자열을 넘겨주는 함수를 쓰도록 약속으로 정해둠
		 
		 */
		
		
	}
}
